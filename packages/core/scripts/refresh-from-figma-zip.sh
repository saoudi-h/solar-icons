#!/usr/bin/env bash
# Refresh packages/core/svgs/ from a ZIP produced by the
# Solar Icon Exporter Figma plugin.
#
# Why this exists:
#   The Figma REST API rate limit on the free plan is 6 GET requests
#   per month (see packages/figma-export-plugin/README.md). The legacy
#   `pnpm generate:svgs` script uses the REST API; the Figma plugin
#   exports locally and bundles the result as a ZIP. This script is
#   the new "refresh svgs/" entry point.
#
# Usage:
#   pnpm refresh:zip /path/to/solar-icons-svgs.zip
#   bash scripts/refresh-from-figma-zip.sh /path/to/solar-icons-svgs.zip
#
# What it does, in order:
#   1. Sanity-checks the ZIP (exists, contains a 'svgs/' directory).
#   2. Wipes packages/core/svgs/.
#   3. Unzips the contents (the ZIP root is the 'svgs/' directory).
#   4. Runs `pnpm generate:svgs --offline` to rebuild
#      src/metadata.json from the new files.
#   5. Removes the ZIP.
#   6. Prints a `git status` summary of the changes.
#
# After running, the typical follow-up is:
#   - `git diff svgs/ src/metadata.json` to inspect the changes.
#   - Remap keys in `src/metadata-descriptions.json` to match any
#     renamed icons (the descriptions file is hand-curated and is
#     not regenerated by the Figma plugin).

set -euo pipefail

ZIP_PATH="${1:-}"
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PACKAGE_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
SVGS_DIR="$PACKAGE_DIR/svgs"

if [ -z "$ZIP_PATH" ]; then
    echo "Usage: pnpm refresh:zip /path/to/solar-icons-svgs.zip" >&2
    echo "   or: bash scripts/refresh-from-figma-zip.sh /path/to/solar-icons-svgs.zip" >&2
    exit 1
fi

if [ ! -f "$ZIP_PATH" ]; then
    echo "Error: ZIP file not found: $ZIP_PATH" >&2
    exit 1
fi

if ! unzip -l "$ZIP_PATH" | grep -q "[[:space:]]svgs/"; then
    echo "Error: ZIP does not appear to contain a 'svgs/' directory at the root." >&2
    echo "       Is this the right file? (It should be the output of the" >&2
    echo "       Solar Icon Exporter Figma plugin.)" >&2
    exit 1
fi

cd "$PACKAGE_DIR"

echo "==> Removing old svgs/ ..."
rm -rf "$SVGS_DIR"

echo "==> Unzipping $ZIP_PATH ..."
unzip -q "$ZIP_PATH"

# Quick sanity check
SVG_COUNT=$(find "$SVGS_DIR" -name "*.svg" | wc -l | tr -d ' ')
echo "    Extracted $SVG_COUNT SVG files."

echo "==> Rebuilding metadata ..."
pnpm generate:svgs --offline

echo "==> Removing ZIP ..."
rm "$ZIP_PATH"

echo ""
echo "==> Done. Changed files:"
git status --short svgs/ src/metadata.json

echo ""
echo "Next steps:"
echo "  - Inspect: git diff svgs/ src/metadata.json"
echo "  - Remap: src/metadata-descriptions.json (hand-curated, not regenerated)"
